def random_date(days_back=60):
    d = datetime(2025, 8, 1) + timedelta(days=random.randint(0, days_back))
    h, m = random.randint(8, 20), random.randint(0, 59)
    return d.replace(hour=h, minute=m)

def make_comment(project_id, author, ctype, idx):
    templates = COMMENT_TEMPLATES[ctype]
    text = random.choice(templates)
    if '{}' in text:
        text = text.format(random.randint(1, 10), random.randint(1, 50))
    dt = random_date()
    date_str = dt.strftime("%d/%m")
    time_str = dt.strftime("%H:%M")
    return {
        "author": author,
        "content": text,
        "timestamp": f"{date_str} - {time_str}",
        "date": date_str,
        "time": time_str,
        "comment_id": f"{author}_{text[:30]}_{date_str} - {time_str}",
    }

def generate():
    projects = []
    total_comments = 0

    # Берём все 50 шаблонов
    templates = PROJECTS_TEMPLATES[:]
    random.shuffle(templates)

    # Целевое распределение комментариев: 1000 на 50 проектов
    # Некоторые проекты популярны (30-40 комментариев), другие — мало (5-10)
    weights = []
    for i in range(len(templates)):
        if i < 8:      weights.append(random.randint(30, 45))   # горячие
        elif i < 20:   weights.append(random.randint(15, 25))   # средние
        else:          weights.append(random.randint(5, 12))    # тихие

    # Нормализуем до ~1000
    total_w = sum(weights)
    counts = [max(3, round(w / total_w * 1000)) for w in weights]
    # Подгоняем к ровно 1000
    diff = 1000 - sum(counts)
    counts[0] += diff

    for i, ((cat, title), n_comments) in enumerate(zip(templates, counts)):
        pid = str(15000000 + random.randint(1000, 99999))
        status = random.choice(STATUSES)

        comments = []
        gov_reply_at = random.randint(2, max(3, n_comments - 1))

        for j in range(n_comments):
            if j == gov_reply_at:
                author = random.choice(GOV_AUTHORS)
                ctype = "government_response"
            elif j % 7 == 0:
                author = random.choice(GOV_AUTHORS)
                ctype = "government_response"
            else:
                author = random.choice(CITIZEN_AUTHORS)
                # тональность: 40% нейтральные, 30% негативные, 30% позитивные
                r = random.random()
                if r < 0.30:    ctype = "positive"
                elif r < 0.60:  ctype = "negative"
                else:           ctype = "neutral"
            comments.append(make_comment(pid, author, ctype, j))

        total_comments += len(comments)
        projects.append({
            "id": pid,
            "title": title,
            "url": f"https://legalacts.egov.kz/npa/view?id={pid}",
            "category": cat,
            "status": status,
            "comments": comments,
            "total_comments": len(comments),
        })

    return projects, total_comments

if __name__ == "__main__":
    projects, total = generate()
    out_path = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
                            "legal_analytics", "demo_data.json")
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(projects, f, ensure_ascii=False, indent=2)
    print(f"Готово: {len(projects)} законопроектов, {total} комментариев → {out_path}")
